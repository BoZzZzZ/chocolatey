<h1>Comment faire face &#xE0; un probl&#xE8;me sur Habbo?</h1>
<p>Tu pourras de temps en temps avoir &#xE0; faire &#xE0; de mauvaises conduites sur Habbo. Mais ne crains rien, l&apos;aide
    est &#xE0; port&#xE9;e de main! Sur cette page, nous allons t&apos;expliquer quels sont les outils qui fonctionnent
    le mieux pour faire face &#xE0; des situations d&#xE9;licates.</p>
<h2>Dans un appart</h2>
<p>Quand tu es dans un appart et qu&apos;un autre Habbo appara&#xEE;t comme hors-ligne, clique sur son avatar pour faire
    appara&#xEE;tre un menu d&#xE9;roulant. Tu pourras ainsi l&apos;ignorer, mod&#xE9;rer ou m&#xEA;me rapporter ce
    parturbateur.</p>
<h4>Ignorer un Habbo</h4>
<p>Si <strong>un Habbo dit des choses qui peuvent te mettre mal &#xE0; l&apos;aise</strong>, tu peux les ignorer. C&apos;est
    la solution id&#xE9;ale contre les taquineries, le spam, ou tout simplement quand tu veux lui dire &quot;Hasta la
    vista&quot; mais tu ne sais pas comment.</p>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/report_FR.png"
        alt="Clique sur un avatar pour l&apos;ignorer, mod&#xE9;rer ou rapporter" class="align-right"></p>
<ol>
    <li>Cliquer sur un avatar. Un menu appara&#xEE;tra.</li>
    <li>Choisir l&apos;option <em>Ignorer</em>.</li>
    <li>Il te sera impossible de voir ce que ce Habbo dit. Tu as chang&#xE9; d&apos;avis et tu ne souhaites plus l&apos;ignorer?
        Clique &#xE0; nouveau sur l&apos;avatar et choisis l&apos;option <em>&#xC9;couter</em>.
    </li>
</ol>
<h4>Mod&#xE9;rer un Habbo</h4>
<p><strong>Si tu es dans ton appart, ou dans un appart dans lequel tu as les droits</strong>, tu pourras d&#xE9;cider
    qui peut visiter l&apos;appart, et tu auras le pouvoir de mettre sous silence, expulser ou bloquer l&apos;acc&#xE8;s
    &#xE0; d&apos;autres utilisateurs. Cela te permet de jouer un r&#xF4;le actif dans la mod&#xE9;ration g&#xE9;n&#xE9;rale
    de Habbo et de contribuer &#xE0; cr&#xE9;er une communaut&#xE9; plus saine et plus agr&#xE9;able.</p>
<p>Pour en savoir plus, lis la FAQ sur <a
            href="https://help.habbo.fr/entries/22589238-Les-nouveaux-outils-de-mod%C3%A9ration-" target="_blank">les
        outils de mod&#xE9;ration</a> et <a
            href="https://help.habbo.fr/entries/38351866-Quels-outils-pour-visiter-l-H%C3%B4tel-en-toute-s%C3%A9curit%C3%A9-"
            target="_blank">les param&#xE8;tres des apparts</a>.</p>
<h4>Rapporter un Habbo</h4>
<p>Si <strong>les choses tournent au vinaigre</strong>: des Habbos parlent de se rencontrer dans la vraie vie, veulent s&apos;appeler
    par cam, &#xE9;changent des donn&#xE9;es personnelles ou si quelqu&apos;un est victime de harc&#xE8;lement&#x2026;
    Tu peux envisager de signaler ces personnes. Attention, utilise cet outil uniquement lorsque c&apos;est n&#xE9;cessaire,
    lorsqu&apos;une personne fait intentionnellement du mal aux autres ou &#xE0; eux-m&#xEA;mes.</p>
<ol>
    <li>Clique sur l&apos;avatar fauteur de troubles pour faire appara&#xEE;tre le menu.</li>
    <li>Clique sur <em>Rapporter</em>.</li>
    <li>S&#xE9;lectionne les paroles &#xE0; rapporter</li>
    <li>Choisis une cat&#xE9;gorie pour le probl&#xE8;me</li>
    <li>Explique au mod&#xE9;rateur ce qui se passe</li>
    <li>Envoie ta demande et un mod&#xE9;rateur essaiera de r&#xE9;soudre le probl&#xE8;me. Si tu choisis <em>Harc&#xE8;lement</em>
        un gardien pourra intervenir.
    </li>
</ol>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/help_button_fr.png" alt="Bouton d&apos;Aide" class="align-right">
</p>
<p>Un autre moyen de rapporter un Habbo:</p>
<ol>
    <li>Clique sur <em>Aide</em> en haut &#xE0; droite.</li>
    <li>Choisis <em>Quelqu&apos;un se comporte mal</em>. Tu verras alors la liste des Habbos dans la salle.</li>
    <li>Choisis l&apos;utilisateur en question.</li>
    <li>S&#xE9;lectionne le tchat &#xE0; rapporter aux mod&#xE9;rateurs.</li>
    <li>Choisis la meilleur description pour la situation.</li>
    <li>&#xC9;cris une courte description pour expliquer ce qui se passe.</li>
    <li>Finalement clique sur <em>Envoyer la demande</em></li>
</ol>
